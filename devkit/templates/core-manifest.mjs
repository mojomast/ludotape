import {CORE_TEMPLATE_VERSION} from './constants.mjs';

/**
 * Render `core.manifest.json` content matching `templates/core.mjs`'s generated core exactly
 * (see `ludotape/core-manifest@1` in CORE_SPEC.md). Deterministic; depends only on `{id, name}`.
 */
export function coreManifestTemplate({id, name}) {
  const manifest = {
    format: 'ludotape/core-manifest@1',
    id,
    version: CORE_TEMPLATE_VERSION,
    name,
    description: 'Document-driven counter core generated by ludotape-create-core.',
    entry: './core.mjs',
    capabilities: {replay: true, rewind: true, solve: true, scenarios: true},
    cartridgeFormats: [`${id}/cartridge@1`]
  };
  return `${JSON.stringify(manifest, null, 2)}\n`;
}
